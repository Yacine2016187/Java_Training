package JavaBasics;

public class Loops_Concept {

	public static void main(String[] args) {
		//1. while loop
		
		int i=1;
		while (i<10) 
		{
			System.out.println(i);
			
			i=i+1;  //incremental part / decremental part 
		}	
		System.out.println("*****************************");
		
		// 2. For Loop
		
			for (int j=1;j<=10;j++) 
		{		
			System.out.println(j);
				
		}
		System.out.println("******************************");
		
			for (int k=10;k>=1;k--)
		{
			System.out.println(k);
		}	
			
}
}


